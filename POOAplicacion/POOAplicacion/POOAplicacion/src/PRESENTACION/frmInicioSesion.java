    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
      String usuario = txtUsuario.getText();
    String password = new String(txtPassword.getPassword());

    if (usuario.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Campos vacíos", JOptionPane.WARNING_MESSAGE);
        return;
    }

    Connection cnx = null;
    PreparedStatement ps = null;
    ResultSet rs = null;

    try {
        cnx = Conexion.obtenerConexion();
        String sql = "SELECT * FROM usuarios WHERE usuario = ? AND password = ?";
        ps = cnx.prepareStatement(sql);
        ps.setString(1, usuario);
        ps.setString(2, password);
        rs = ps.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso. Bienvenido, " + usuario);
            // Aquí puedes abrir tu ventana principal o pasar al siguiente formulario
            // new MenuPrincipal().setVisible(true);
            // this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos.", "Error de autenticación", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al intentar conectar con la base de datos: " + e.getMessage(), "Error de conexión", JOptionPane.ERROR_MESSAGE);
    } finally {
        try {
            if (rs != null) rs.close();
            if (ps != null) ps.close();
            Conexion.cerrarConexion(cnx);
        } catch (SQLException ex) {
            System.out.println("Error al cerrar la conexión: " + ex.getMessage());
        }
    }  
        
        
        
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

