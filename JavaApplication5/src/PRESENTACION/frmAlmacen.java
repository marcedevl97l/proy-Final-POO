    private void btnInsertarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarRegistroActionPerformed
        // cargar datos
        String nombreProducto = txtProducto.getText().trim();
    if (!nombreProducto.isEmpty()) {
        Producto producto = new Producto(nombreProducto);
        ProductoDAO dao = new ProductoDAO();
        if (dao.insertarProducto(producto)) {
            JOptionPane.showMessageDialog(this, "‚úÖ Producto agregado");
            txtProducto.setText("");
            cargarTablaProductos(); // actualiza la tabla
        } else {
            JOptionPane.showMessageDialog(this, "‚ùå Error al agregar producto");
        }
    } else {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Ingresa un nombre de producto");
    }
    }//GEN-LAST:event_btnInsertarRegistroActionPerformed

